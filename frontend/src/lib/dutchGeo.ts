// ---------------------------------------------------------------------------
// Matching helper
// ---------------------------------------------------------------------------

/**
 * Normalize a string for comparison: lowercase, strip leading #,
 * remove spaces, hyphens, and underscores.
 */
function normalize(s: string): string {
  return s
    .toLowerCase()
    .replace(/^#+/, '') // strip leading hash(es)
    .replace(/[\s\-_]/g, '') // remove spaces, hyphens, underscores
    .trim();
}

/**
 * Match a MeshCore channel name (e.g. "#amsterdam", "noord-holland", "rijnmond")
 * against the DUTCH_GEO dataset.
 *
 * Matching order:
 *  1. Exact match on normalized canonical name
 *  2. Exact match on any alias
 *
 * Returns the first matching entry or null.
 */
export function matchDutchChannel(channelName: string): DutchGeoEntry | null {
  const key = normalize(channelName);

  if (!key) return null;

  for (const entry of DUTCH_GEO) {
    if (normalize(entry.name) === key) return entry;
  }

  for (const entry of DUTCH_GEO) {
    for (const alias of entry.aliases) {
      if (normalize(alias) === key) return entry;
    }
  }

  return null;
}

// ---------------------------------------------------------------------------
// Auto-fill helper
// ---------------------------------------------------------------------------

export interface ChannelAutoFill {
  country: string;
  region: string;
  scopes: string[];
  notes: string;
  category: string;
  subcategory: string;
}

/**
 * Build an auto-fill object from a DutchGeoEntry, suitable for populating
 * a channel registry form.
 *
 * The veiligheidsregio is added to scopes with a "VR " prefix so it can be
 * styled distinctly (see isVeiligheidsregio below).
 */
export function buildAutoFillFromGeo(entry: DutchGeoEntry): ChannelAutoFill {
  const vrScope = entry.veiligheidsregio_code;
  const scopes = entry.scopes.includes(vrScope) ? entry.scopes : [...entry.scopes, vrScope];
  return {
    country: 'Netherlands',
    region: entry.province,
    scopes,
    notes: entry.notes ?? '',
    category: 'Regional',
    subcategory: entry.type === 'province' ? 'Province' : 'City',
  };
}

/**
 * Returns true if the given scope string is a Dutch veiligheidsregio.
 * These are stored as "vr-<slug>" in the scopes array (e.g. "vr-gelderland-zuid").
 * Used to render VR scopes with a distinct red/orange badge in the UI.
 */
export function isVeiligheidsregio(scope: string): boolean {
  return scope.startsWith('vr-');
}
